$(document).ready(function(){
    $('#generated-btn').click(()=>{
        $('#initial-choices').hide();
        $('#generated-graph').show();
    });
    $('#consumed-btn').click(()=>{
        $('#initial-choices').hide();
        $('#consumed-graph').show();
    });
    $('#switch-g-btn').click(()=>{
        $('#generated-graph').hide();
        $('#consumed-graph').show();
    });
    $('#switch-c-btn').click(()=>{
        $('#consumed-graph').hide();
        $('#generated-graph').show();
    });
})